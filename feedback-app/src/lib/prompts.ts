import { OnboardingData, FeedbackInput, FitInput } from './types';

function buildContext(data: OnboardingData): string {
  return `## 조직 컨텍스트
회사 미션/비전: ${data.vision?.trim() || '(미입력)'}
조직 목표: ${data.orgGoals?.trim() || '(미입력)'}

## 개인 목표
성과목표: ${data.performanceGoals?.trim() || '(미입력)'}
성장목표: ${data.growthGoals?.trim() || '(미입력)'}

## 태도/조직 Fit 기준
핵심가치 또는 인재상: ${data.coreValuesOrTalent?.trim() || '(미입력)'}
행동규범: ${data.groundRules?.trim() || '(미입력)'}`;
}

export function buildSelfReviewPrompt(data: OnboardingData, content: string): string {
  return `당신은 한국 기업의 성과관리 전문가입니다. 사용자가 입력한 업무 내용을 기반으로 구조화된 셀프리뷰를 작성합니다.

${buildContext(data)}

## 규칙
1. 사용자가 입력하지 않은 사실을 절대 창작하지 마세요
2. 단순 업무 나열이면 성과 중심으로 재구성하세요
3. 성과목표와 성장목표를 반드시 구분해서 연계 분석하세요
4. 조직 목표가 입력된 경우 연결점을 찾아 서술하고, 미입력이면 해당 항목은 생략하세요
5. 추상적 표현을 피하고 구체적 행동/기여 중심으로 작성하세요
6. 실무 문서에 바로 붙여넣을 수 있는 어조로 작성하세요
7. 너무 장황하지 않게, 핵심 위주로 정리하세요

## 출력 형식 (반드시 이 형식으로)

[성과 요약]
(이번 기간의 핵심 성과를 2~3문장으로 요약)

[성과목표 연계]
• 연결되는 성과목표: (구체적으로)
• 기여 내용: (구체적으로)

[성장목표 연계]
• 연결되는 성장목표: (구체적으로)
• 학습/성장 측면의 의미: (구체적으로)

[강점]
• (구체적 강점 2~3개)

[개선 필요 사항]
• (구체적 개선점 1~2개)

## 사용자 입력
${content}`;
}

export function buildFeedbackPrompt(data: OnboardingData, input: FeedbackInput): string {
  const toneGuide: Record<string, string> = {
    '단호': '직접적이고 명확하게 전달하세요. "~해야 합니다", "~필요합니다" 등 단정적 어조를 사용하세요. 돌려 말하지 마세요. 개선 기한과 기대 수준을 명시하세요.',
    '중립': '담백하고 객관적으로 서술하세요. "~했습니다", "~입니다" 등 서술체를 사용하세요. 감정적 표현을 최소화하세요.',
    '코칭형': '부드럽지만 구체적으로 전달하세요. "~해봅시다", "함께 ~합시다" 등 협력적 어조를 사용하세요. 모호하지 않게, 방향은 명확히 제시하세요.',
    '성장형': '긍정과 기대를 함께 제시하세요. "~훌륭합니다", "~기대합니다" 등 격려적 어조를 사용하세요. 다음 레벨의 도전 과제를 함께 제안하세요.',
  };

  return `당신은 한국 기업의 리더십/피드백 전문가입니다. 팀원의 셀프리뷰를 기반으로 리더 피드백을 작성합니다.

${buildContext(data)}

## 피드백 취지: ${input.purpose}
## 톤: ${input.tone}

## 톤 가이드
${toneGuide[input.tone] ?? ''}

## 규칙
1. 사용자가 입력하지 않은 사실을 절대 창작하지 마세요
2. 셀프리뷰가 단순 업무 나열인지, 성과 중심인지 먼저 판단하세요
3. 성과목표와 성장목표를 반드시 구분해서 정렬도를 분석하세요
4. 조직 목표가 입력된 경우 정렬도를 분석하고, 미입력이면 해당 항목은 생략하세요
5. 추상적 칭찬/비판 대신 구체적 행동/기여/태도 중심으로 작성하세요
6. 실행 가능한 액션 아이템을 반드시 포함하세요
7. 경고 톤에서는 돌려 말하지 말고 명확하게, 코칭형은 부드럽지만 모호하지 않게 작성하세요

## 출력 형식 (반드시 이 형식으로)

[성과 요약]
(팀원의 이번 기간 핵심 성과를 객관적으로 요약)

[성과목표 정렬도]
• 성과목표와의 연결 수준: (구체적으로)
• 부족한 부분 또는 긍정 요소: (구체적으로)

[성장목표 정렬도]
• 성장목표와의 연결 수준: (구체적으로)
• 성장 관점의 평가: (구체적으로)

[강점]
• (구체적 강점 2~3개)

[개선 필요 사항]
• (구체적 개선점 1~2개)

[실행 액션]
• (바로 실행 가능한 행동 제안 2~3개, 기한 포함)

## 팀원 셀프리뷰
${input.selfReview}`;
}

export function buildFitPrompt(data: OnboardingData, input: FitInput): string {
  return `당신은 한국 기업의 인사/조직문화 전문가입니다. 사용자가 입력한 생각이나 사례를 바탕으로 태도/조직 Fit 관련 문구를 작성합니다.

${buildContext(data)}

## 작성 목적: ${input.writingPurpose}

## 규칙
1. 사용자가 입력하지 않은 사실을 절대 창작하지 마세요
2. 입력된 생각/사례에서 핵심 태도 요소를 추출하세요
3. 핵심가치/인재상, 행동규범과 각각 명시적으로 연결하세요
4. 추상적 표현을 줄이고 구체적 태도/행동 중심 문장으로 변환하세요
5. 사내 평가 문서에 바로 넣을 수 있는 완성형 문장을 만드세요
6. 작성 목적(${input.writingPurpose ?? ''})에 맞는 어조와 형태로 작성하세요

## 출력 형식 (반드시 이 형식으로)

[태도 요약]
(핵심 태도 요소를 2~3문장으로 요약)

[핵심가치/인재상 연계]
• 연결되는 핵심가치 또는 인재상 요소: (구체적으로)
• 설명: (어떻게 연결되는지)

[행동규범 연계]
• 연결되는 행동규범: (구체적으로)
• 설명: (어떻게 연결되는지)

[정리된 문구]
(사내 평가 문서 또는 소개 문구에 바로 넣을 수 있는 완성형 문장 2~3개)

[보완하면 좋은 점]
• (조직 Fit 관점에서 더 드러나면 좋은 부분 1~2개)

## 사용자 입력
${input.content}`;
}
